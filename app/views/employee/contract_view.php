            <li style="margin-bottom: 10px;">الطرف الأول: هو مؤسسة " أسفار & أعمال" – والحاصلة على عقد التشغيل المؤقت للمرحلة من عملية تشغيل محطات قطار المشاعر المقدسة – الخط الجنوبي- MMMP- لموسم الحج عام 1431هـ - والحاصلة على عقد جزئي من عملية التشغيل لهذا العام 1432/1433هـ (للمرة الثالثة / الموسم الثالث على التوالي) .</li>
            <li style="margin-bottom: 10px;"> الطرف الثاني / هو المتقدم بطلب التوظيف الموسمي- وتم اجتيازه لمراحل الفرز الأولى والنهائي- وأكمل جميع الشروط المطلوبة- ووافق على بنود الاتفاقية والالتزام والعقد، والمستفيد النهائي لقيمة المكافأة الموسمية- وتم توقيعه لهذا العقد.</li>
            <li style="margin-bottom: 10px;">العملية/ التشغيل الموسمي لمحطات وقطار (مترو) المشاعر المقدسة- وذلك بتحضير الأعداد المطلوبة من الشركة المانحة للعقد ((CRCC))- من الموظفين المتدربين والمؤهلين لعملية التشغيل.</li>
            <li style="margin-bottom: 10px;">التشغيل/ تحضير الموظفين وتأهيلهم وتدريبهم/ وذلك لشغل الوظائف والتخصصات والأعمال الخاصة بعملية التشغيل/ دورات تدريبية تحت تنفيذ CRCC وإشراف مشترك مع " أسفار& أعمال". </li>
            <li style="margin-bottom: 10px;"> العمل/ هو بالتحديد تواجد الطرف الثاني في الموقع المعين عليه من قبل CRCC/خلال ساعات العمل المعينة عليه/ وأداء العمل المطلوب منه حسب ما تدرب عليه خلال التدريبات: النظرية والعملية/ وتنفيذ جميع خطوات العمل وتحركاته حسب الخطط الموضوعة والتعليمات والقرارات الصادرة من CRCC إدارة " أسفار & أعمال " أو من جهاز الإشراف الإداري/ العملي- حتى يستحق على أدائه : مكافأة الموسم .</li>
            <li style="margin-bottom: 10px;">المكافأة : هي مكافأة أعمال الموسم وتشمل فترة التدريب المقررة + فترة أعمال التشغيل المقررة/ تقوم إدارة " (CRCC) بتسليمها للطرف الثاني بعد نهاية أعمال التشغيل الموسمي بالكامل- بعد حساب ساعات التأخير والغياب وتسليم العهد واعتماد أسفار وأعمال لبيان الحضور / حسب قوانين شركة CRCC .</li>
            <li style="margin-bottom: 10px;">العهدة/ هي كل ما يستلمه الموظف من / أطقم ملابس العمل- بطاقة تعريفية- تصريح دخول المحطات – رتب تعريفية- لوحات تعريف الوظيفة- // وللتنبيه// يتم خصم قيمة كل قطعة من " العهدة " لا تتم استعادتها – حسب جدول العقوبات الموضوع من شركة CRCC. </li>
            <li style="margin-bottom: 10px;">
                <div>العقوبات المالية / وهي مقادير الجزاءات والخصومات المالية المترتبة على :</div>
