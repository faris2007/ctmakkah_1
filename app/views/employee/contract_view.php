                <div>* عدم الانصياع لتعليمات المسؤولين. </div>
                <div>* ملاحظات/ سوء السلوك- المقررة من إدارة "أسفار & أعمال" أو شركة CRCC أو أي جهة أخرى لها علاقة بأعمال الحج . </div>
                <div>* شروط العقد: </div>
                <div>- الطرف الأول: </div>
                <ul style="list-style: square;">
                    <li>يقوم الطرف الأول بتوفير/ (مقعد تدريب) للطرف الثاني خلال فترة التدريبات التي تقوم بها الشركة المانحة للعقد CRCC . </li>
                    <li>يقوم الطرف الأول بتوفير/ بطاقة- تصريح دخول لمواقع العمل للطرف الثاني ( لفترة العمل فقط)- حسب المستلم من قبل شركة CRCC</li>
                    <li>يقوم الطرف الأول بتوفير/ طقم الملابس بالقياسات الملائمة للطرف الثاني/ حسب توفيرها من CRCC (لفترة العمل فقط) . </li>
                    <li>يقوم الطرف الأول بتوفير/ الإعاشة + المواصلات+ السكن ( حسب شروط CRCC ) للطرف الثاني ( لفترة العمل/ الحج فقط). </li>
                    <li>يقوم الطرف الأول بتشغيل/ الطرف الثاني – بما يتلاءم مع تدريباته في أي موقع حسب متطلبات العمل. </li>
                    <li>تقوم شركة CRCC وبمشاركة الطرف الأول بتسليم / مكافأة الموسم- للطرف الثاني – بعد نهاية أعمال الموسم والانتهاء من حسابات" العقوبات المالية" والحوافز التشجيعية . </li>
                    <li>يقوم الطرف الأول بضمان استلام الطرف الثاني لشهادة الدورة التدريبية المعتمدة من CRCC (مجاناً بدون مقابل) . </li>
                </ul>
                <div>- الطرف الثاني : </div>
                <ul style="list-style: square;">
                    <li>يقر الطرف الثاني بمعرفته التامة وإلمامه بالمصاعب والمعوقات في موسم الحج ويتعهد الطرف الثاني/ بالحضور المبكر والمنتظم خلال فترة الدورة التدريبية/ دون تأخير أو غياب إلا في حالة الضرورة. وتبليغ مشرفي التدريب مسبقاً من قبل شركة CRCC . </li>
                    <li>يتعهد الطرف الثاني/ بالاللتزام الكامل بالتحلي بالأخلاق الحميدة والتعامل الرفيع خلال فترة التدريب+ فترة العمل حتى توقيع المخالصة مع " أسفار & أعمال" بعد نهاية الموسم . </li>
                    <li>يتعهد الطرف الثاني/ بالالتزام بالعمل بما يحقق نجاح أعمال التشغيل الموسمي لعام 1433هـ . </li>
                    <li>يتعهد الطرف الثاني/ بالحضور المنتظم خلال فترة العمل الفعلية وعلى الخصوص أيام الحج الفعلية دون تأخير أو غياب . </li>
                    <li>يلتزم الطرف الثاني بالحضور إلى العمل بالملابس الخاصة بالعمل( بحالة نظيفة ومرتبة مكوية) مع استخدام (حذاء جلد أسود يتلاءم مع متطلبات السلامة والمنظر العام). </li>
                    <li>يمتنع الطرف الثاني امتناعاً قاطعاً بإساءة السلوك أو الأدب أو اللفظ أو التلميح إلى أي شخص كان – طالما كان الطرف الثاني يلبس ملابس العمل- سواء كان داخل حدود العمل أو خارجه - . </li>
                    <li>يتعهد الطرف الثاني/ بتسليم" العهدة" بعد نهاية أعمال التشغيل مباشرة – بحالة جيدة وبترتيب جيد (بعد التغسيل والكي للملابس) بتاريخ 20/12/1433هـ كحد أقصى. </li>
                    <li>يتعهد الطرف الثاني / باللجوء إلى القنوات الإدارية عند وجود أي مشكلة- أو استفسار بما يتعلق بالعملية . </li>
                    <li> يتعهد الطرف الثاني / بالحضور لاستلام "المكافأة" عند المواعيد المحددة " لاحقاً" وحسب الجداول الموضوعة لعملية التسليم من قبل الشركة المانحة CRCC .</li>
                    <li>يمتنع الطرف الثاني عن التدخين داخل المحطات. </li>
                </ul>
